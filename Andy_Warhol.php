<h1>Andy Warhol</h1>
<p>Andrew Warhola (6 août 1928 au 22 février 1987), connu sous le nom d'Andy Warhol, était un artiste américain et une figure centrale dans le mouvement artistique du Pop art. Après une carrière réussie en tant qu'illustrateur commercial, Warhol est devenu célèbre dans le monde entier pour son travail en tant que peintre, réalisateur de films avant-gardistes, producteur de musique et auteur.<br />

<br />Warhol a été le sujet de nombreuses expositions rétrospectives, de livres et de documentaires depuis sa mort en 1987. Warhol a inventé l'expression « 15 minutes of fame » (15 minute de gloire), qui se rapporte à l'état passager de la célébrité. Cette célébrité est rattachée à l'attention des médias sur un sujet, qui s'évapore aussitôt que l'attention est portée sur un nouveau sujet.<br />

<br />La première exposition Pop art solo d'Andy Warhol a été accueillie à la galerie Eleanor Ward's Stable de New York, du 6 au 24 novembre 1962. Les oeuvres exposées incluaient Marilyn Diptych, 100 Soup Cans, 100 Coke Bottles et 100 Dollar Bills. À cette exposition, l'artiste a rencontré pour la première fois John Giorno qui tiendrait le premier rôle dans le premier film de Warhol, Sommeil, en 1963.<br />

<br />C'est au cours des années 1960 que Warhol a commencé à faire des peintures iconiques de produits américains tels que Campbell's Soup Cans de la Campbell Soup Company et les bouteilles Coca-Cola, ainsi que des tableaux de célébrités telles que Marilyn Monroe, Troy Donahue, et Elizabeth Taylor.<br /></p>
