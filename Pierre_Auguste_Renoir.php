<h1>Pierre Auguste Renoir</h1>
<p>Pierre-Auguste Renoir, plus connu sous le nom d'Auguste Renoir, est né en 1841 à Limoges. Grand peintre figuratif français, il privilégie les portraits et les nus, influencé notamment par les tableaux d'Eugène Delacroix. Il s'inspire aussi tout au long de sa carrière des oeuvres de Raphaël, de Fragonard, de Courbet et de Manet. Il compte parmi les plus grands représentants du mouvement impressionniste français. Renoir commence comme apprenti dans un atelier de porcelaine. Puis il entre en 1862 à l'école des beaux-arts de Paris, où il côtoie de grands noms comme Monet, Bazille et Sisley, avec qui il se lie d'amitié. Dès la fin des années 1860, il peint régulièrement en plein air, dans la forêt de Fontainebleau. Cette habitude marque particulièrement son style et ses sujets.<br />

<br />C'est en 1877, alors qu'il expose depuis plusieurs années déjà au salon des impressionnistes, qu'il réalise son célèbre tableau "Le Bal du moulin de la Galette". Après une période de pauvreté, il revient petit à petit vers le portrait et connaît un immense succès en réalisant le "Déjeuner des canotiers". En 1890, trois ans après les "Grandes Baigneuses", il épouse Aline Charigot. Dès lors, il peint dans un style à mi-chemin entre l'impressionnisme et le style ingresque. Il réalise "Les Jeunes Filles au piano", peinture fondatrice de cette nouvelle période. À partir de 1900, Renoir acquiert une solide renommée et expose dans toutes les capitales européennes. Il meurt en 1919, reconnu et comblé. Il est le père de cinq enfants, dont Jean Renoir, célèbre réalisateur et scénariste français.<br /></p>
