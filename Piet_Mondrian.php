<h1>Piet Mondrian</h1>
<p>Piet Mondrian (1872 -1944) fut l'un des pionniers les plus influents de la peinture abstraite. Mondrian qui est entre a l'Academie Nationale des Beaux-Arts d'Amsterdam en 1892, subit d'abord l'influence de l'Ecole de La Haye, en travaillant la forme, le style, la lumiere et l'espace par des oeuvres naturalistes et des paysages academiques, tel un tableau "Au Stadhouderskade Amsterdam " peint en 1898, representant dans des tonalites bleues et grises les bords d'un canal .<br />

<br />Piet Mondrian est alors un peintre realiste tres soucieux de representer la realite et la nature dans tous ses aspects, car c'est elle qui permet l'acces aux revelations spirituelles et aux "verites superieures", selon une pensee traditionnelle liee au protestantisme. Natures mortes et paysages de campagne sont ses sujets dominants, mais il construit a partir de 1897, des oeuvres faites de lignes claires, par l'utilisation qu'il developpe du crayon noir, de l'aquarelle, de la gouache, dans des dominantes bleues, grises ou brunes, dans des oeuvres souvent melancoliques.<br />

<br />A partir de 1904, sa peinture change et les paysages laissent la place a des scenes d'interieurs avec l'abandon des tonalites nuancees au profit de longs aplats de couleurs. Sous l'influence de peintres tels que Cornelius Spoor, Jan Sluyters ou Jan Toorop, il se tourne progressivement vers le fauvisme comme en temoigne des oeuvres telles que "Le nuage rouge" de 1907, "Moulin dans la clarte du soleil "en 1908, ou encore "Devotion", "Crepuscule", "L'arbre bleu". Mais il s'interresse aussi au divisionnisme de Signac, dans des oeuvres telles que "Dunes",  ou un certain expressionnisme de Van Gogh dans une oeuvre telle que " Le Pommier en bleu".<br />

<br />Il poursuit galement une reflexion philosophique sur le role de l'artiste, en quete de permanence et d'universel, et considere que l'artiste doit saisir l'essence des choses, et s'echapper du matrialisme qui conduit  a la simple retranscription ou imitation de la nature, dans les limites de la perception .<br />

<br />Apres des toiles qui celebrent la couleur, succedent des Å“uvres dans lesquelles Mondrian explore avant tout les lignes. "Phare Westkapelle" realise en 1910 fait entrevoir la direction que Mondrian prendra ensuite. "Je trouve vraiment que la grande ligne est l'element primordial dans une chose, c'est ensuite  que vient la couleur, "dit-il.. <br />

<br />Adepte de theosophie, il s'engage dans des compositions fondees sur l'esoterisme et la spiritualite tel que dans l'enigmatique triptyque " Evolution " qu'il peint en 1910, et represente l'evolution de la femme entre la realite matetielle vers la verite mystique. Car Mondrian cherche a faire de sa peinture un langage universel, dont il considere qu'elle doit  rendre compte des realites essentielles au-dela des apparences sensibles. <br />

<br />Lors d'une exposition a Amsterdam en 1912, il decouvre le cubisme et decide alors de s'installer a Paris pour rejoindre le groupe des peintres "cubistes". C'est au cours de cette periode parisienne, entre 1912 et 1914, qu'il passe d'un cubisme figuratif a un cubisme plus abstrait dans lequel les objets perdent leur fonction figurative pour un ensemble de lignes formelles horizontales et verticales, melees aux couleurs primaires dans un espace a deux dimensions.<br />

<br />Il poursuit l'exploration des memes themes, comme les arbres et les facades d'immeubles, telles "Pommier en fleur " , en 1912, et "Composition ovale en plans de couleurs" en 1914.
"Je sentis que seuls les cubistes avaient decouvert le bon chemin, et pendant longtemps, je fus tres influence par eux", ecrira plus tard Mondrian.<br />

<br />En 1914, il retourne aux Pays-Bas pour l'ete, mais la declaration de la guerre l'empeche de revenir a Paris. Il connait alors une renomme internationale, car il est considere comme l' inventeur d'une forme d'art "universelle".<br /></p>
