<h1>Claude Monet</h1>
<p>Claude Monet est un peintre français connu pour son apport dans le mouvement artistique appelé impressionnisme. Né à Paris le 14 novembre 1840, il passe son enfance en Normandie, dans la ville du Havre puisqu’à ses cinq ans sa famille s’y installe. Progressivement le jeune Monet va développer sa passion, en commençant d’abord par la caricature, puis il va étudier la peinture à Paris en 1859 à l’Académie suisse, appuyé par son père. Son départ en Algérie pour le service militaire en 1861 entrecoupe ses études mais il continue à expérimenter différents effets artistiques. De retour à Paris en 1862 suite à une maladie qu’il contracte (la pleurésie), il rencontre le peintre suisse Charles Gleyre et travaille avec Alfred Sisley, Auguste Renoir et Frédéric Bazille qui deviendront ses proches amis.<br />

<br />Durant la seconde partie des années 1860, Monet peint dans un style qui rappelle celui d’Édouard Manet. A mesure qu’il étoffe son style, Claude Monet va être confronté à des difficultés financières car aucun Salon ne voudra exposer ses toiles, malgré le succès de La Femme en robe verte qui représente Camille Doncieux, la femme qu’il épousera en 1870. Par la suite, Monet se rend à Londres au moment de la guerre franco-prussienne et rencontre le marchand Paul Durand-Ruel qui lui achètera ses toiles et contribuera à la diffusion des œuvres des impressionnistes.<br />

<br />Une partie du futur groupe d’impressionnistes se rejoint en 1872 à Argenteuil - composée de Monet, Manet et Renoir - et la première toile issue de ce mouvement artistique sera Impression, soleil levant, qui donnera d’ailleurs son nom au mouvement. Cette peinture sera présentée au public lors de la première exposition impressionniste en 1874. C’est cette année qui marquera l’apogée de l’impressionnisme et définira Claude Monet comme étant l’un des créateurs du mouvement. Malgré les critiques satiriques, six expositions seront présentées jusqu’en 1882 par les impressionnistes. Les achats continus de Durant-Ruel permettront à Claude Monet de vivre décemment sans avoir besoin de participer aux Salons officiels.<br />

<br />En 1883 il finit par déménager à Giverny avec son amie Alice Hoschedé, les six enfants qu’elle a eus avec Ernest Hoschedé et ses deux enfants à lui, dont le dernier n’a pas connu sa mère, morte en couche en 1879. Giverny sera un véritable havre de paix pour Monet. Malgré les nombreux voyages qu’il effectuera pour s’inspirer de différents paysages français, il finira par acheter en 1890 la maison dans laquelle il habitait à Giverny et ses jardins l’inspireront à créer des œuvres aujourd’hui réputées comme la série des Nymphéas. Monet décède dans sa maison givernoise le 5 décembre 1926, entouré par sa famille. Suite à cela, une fondation sera créée pour entretenir le domaine de Giverny et l’ouvrir au public.<br /></p>
