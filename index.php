<h1>L'oeuvre coup de coeur de la semaine</h1>
<p class="tableau">"Jaune-Rouge-Bleu" de Wassily Kandinsky -1925 - Centre Georges Pompidou, Paris</p>
<p>Cette aquarelle, qui serait le premier tableau abstrait au monde, a soulevé controverses et discussions, et suscité bien des thèses contradictoires. Deux tendances s'opposent : celle qui la date réellement de 1910, et qui l'inclut dans une série d'études pour Composition VII, celle qui rejette cette datation et la rapproche des œuvres et du style de 1913. Plaident pour cette dernière thèse le format inhabituel et trop grand pour l'époque, et surtout une inscription tardive de cette œuvre dans le registre manuscrit que tient régulièrement Kandinsky à partir de 1919.</p>
