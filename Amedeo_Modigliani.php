<h1>Amedeo Modigliani</h1>
<p>Amedeo Clemente Modigliani, né le 12 juillet 1884 à Livourne, Italie et mort le 24 janvier 1920 à Paris, est un peintre figuratif et sculpteur italien de l'École de Paris. Se considérant initialement comme un sculpteur, à la suite de sa rencontre avec Constantin Brancusi, ce n'est qu'à partir de 1914 qu'il se consacre exclusivement au dessin et à la peinture de nus et de portraits. Ses œuvres, aux formes étirées et aux visages sans regard ressemblant à des masques, demeurent emblématiques de l'art moderne de cette époque. Il meurt prématurément, perdu d'alcool ; le lendemain, sa compagne, Jeanne Hébuterne, enceinte de neuf mois, se suicide1.<br /></p>
