<h1>Edvard Munch</h1>
<p>Né à Adalsbruk (Norvège) le 12/12/1863 ; Mort à Oslo (Norvège) le 23/01/1944<br />

<br />Figure de proue de l'expressionnisme au cœur de la peinture moderne, Edvard Munch est un peintre norvégien du XIXe siècle. Ses œuvres les plus connues, "Le Cri" et "La Madone", marquent l'apogée de son talent, dans les années 1890. Ses peintures reconnues font de lui un artiste célèbre en Europe et dans le monde entier. Les thématiques de la mort, de la tristesse et de la maladie qui ont ponctué sa vie et celle de sa famille imprègnent ses tableaux.<br />

<br />Edvard Munch poursuit des études techniques avant d'intégrer l'école royale de dessin, où il étudie les anciens maîtres. Aux côtés du naturaliste Christian Krohg, il développe son talent pour le réalisme français. Grâce à des bourses d'études, le visionnaire peut voyager. L'artiste s'installe à Paris, où il désire peindre les affres de son existence, puis à Berlin. Il côtoie de nombreux artistes et se fait réellement connaître grâce à une exposition en 1892. L'artiste développe les sujets et multiplie les techniques. Il entretient par ailleurs une relation amoureuse difficile avec Tulla Larsen.<br />

<br />Les nazis qualifient son art de "dégénéré" et brûlent près de 80 de ses œuvres. Dépressif, il sombre dans l'alcoolisme et séjourne six mois en clinique à Copenhague. Il continue à peindre jusqu'à la fin de sa vie, qu'il termine de façon solitaire. Décédé à 80 ans, l'artiste laisse derrière lui plus d'un millier de tableaux, dessins et sculptures. Son visage apparaît sur les billets de 1000 krones, dans la monnaie norvégienne.<br /></p>
