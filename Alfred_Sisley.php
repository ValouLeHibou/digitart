<h1>Alfred Sisley</h1>
<p>Peintre paysagiste d' origine anglaise, né à Paris, mort à Moret-sur-Loing. Son père était un riche négociant anglais installé à Paris, où il mourut ruiné en 1870. Les débuts du peintre furent heureux, car son père, après lui avoir donné une excellente éducation, ne s'opposa point à sa vocation de peintre. Entré à l'atelier de Gleyre où il se lie avec Monet, Renoir, Bazille. Monet entraîne ses amis à Chailly à la lisière de la forêt de Fontainebleau et Sisley y trouve l'inspiration de ses premiers paysages. Sisley aide volontiers ses amis et Renoir a peint de charmants portraits de Sisley et de sa femme, donnant l'image d'une famille heureuse. <br />

<br />Mais après la mort de son père et la ruine matérielle qui s'ensuivit, sa vie ne fut alors qu'une longue lutte contre l'adversité et la nécessité. Quoique mêlé de très près à l'histoire du groupe impressionniste, puisqu'il était l'ami de tous et peignait avec les mêmes conceptions, bien qu'il ait partagé les attaques dont ils étaient l'objet, par malchance, il n'obtint jamais la même audience, le même succès.<br />

<br />Pour lui, comme pour ses amis, la vente aux enchères de 1875 fut une défaite. En 1878, il suppliait Duret de lui verser pendant six mois la somme de 500 francs en échange de trente toiles. Duret ne put lui venir en aide que partiellement. L'art de Sisley est fait de délicatesse et de modestie. S'il sut conserver à ses paysages une structure, il se plia toujours aux nécessités de son thème sans esprit systématique, ni dogmatique, laissant libre cours à sa sensibilité. Il travailla essentiellement dans la vallée de la Seine et ses environs : Marly, Bougival, Louveciennes (1872-1876), Sèvre, Suresne, Sait-Mammès (1877-1882). Il se retira dans la petite ville de Moret où il résida jusqu'à sa mort.<br />

<br />En 1883, Durand-Ruel lui consacra une grande exposition. A partir de1885, il se rapprocha de Monet et adopta une palette voisine de la sienne et la "technique" impressionniste des hachures souples. Pour Sisley, le ciel est la clé principale du paysage; en cela, il se montre l'héritier de Corot dont il admirait les oeuvres. <br />

<br />Vivement affecté par le peu d'intérêt qui lui était manifesté, Sisley devint difficile, irritable et ombrageux. Pourtant la soif de peindre, la joie de travailler, ne l'abandonnèrent jamais.
A sa mort seulement, on reconnut ses mérites et il trouva le renom que toute sa vie lui avait refusé. <br /></p>
