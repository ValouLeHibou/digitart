<h1>Rene Magritte</h1>
<p>René Gislain Magritte est né le 21 novembre 1898 à Lessines en Belgique dans une famille modeste. Son enfance sera marquée par le suicide de sa mère. A l'âge de quinze ans, il rencontre Georgette Berger qu'il épousera en 1922. Pendant toute sa vie , elle demeurera son unique modèle.<br />

<br />En 1915 Magritte s'installe à Bruxelles et réalise ses premières oeuvres de facture impressionniste. Il étudie à l'Académie des Beaux-Arts de Bruxelles de 1916 à 1918. Après une première période influencé par le cubisme, il s'oriente vers le surréalisme. Il travaille comme graphiste dans une usine de papiers peints.<br />

<br />Dès lors, son art tentera de démontrer que l'on n'entrevoit de la réalité que son mystère, si l'on sort de notre habituelle et routinière logique. Ses œuvres jouent souvent sur le décalage entre un objet et sa représentation.<br />

<br />De 1927 à 1930 il séjourne dans la banlieue parisienne et entre en contact avec le groupe. A l'atmosphère insolite de certaines réalisations surréalistes, Magritte ajoute toute une gamme d'objets familiers.<br />

<br />De retour en Belgique, il devient le chef de file des surréalistes belges. Entre 1951 et 1953, il décore d'une immense fresque le casino de Knokke-le-Zoute. Le reste de sa vie se passe en expositions belges et internationales...<br />

<br />Magritte meurt à Bruxelles le 15 août 1967, âgé de 69 ans, après avoir peint plus de 1000 toiles, ainsi que des gouaches et des collages. Son épouse léguera les oeuvres de son mari à diverses collections publiques belges.<br />
</p>
