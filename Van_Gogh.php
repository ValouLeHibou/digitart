<h1>Van Gogh</h1>
<p>Vincent Willem van Gogh, né le 30 mars 1853 à Groot-Zundert (Pays-Bas) et mort le 29 juillet 1890 à Auvers-sur-Oise (France), est un peintre et dessinateur néerlandais. Son œuvre pleine de naturalisme, inspirée par l'impressionnisme et le pointillisme, annonce le fauvisme et l'expressionnisme. <br />
<br />Artiste non reconnu et incompris de son vivant, Van Gogh est l’un des grands peintres du XIXème siècle. Hésitant un temps entre la vocation artistique et religieuse, il choisit de se consacrer à la peinture. On peut dire aujourd’hui qu’il a eu raison. Son style très coloré a une vitalité et une tension particulière qui n’ont pas fini de marquer les esprits <br />
<br />L'œuvre de Van Gogh est composée de plus de deux mille toiles et dessins datant principalement des années 1880. Elle fait écho au milieu artistique européen de la fin du xixe siècle. Il est influencé par ses amis peintres, notamment Anthon van Rappard, Émile Bernard et Paul Gauguin. Il échange aussi des points de vue avec son frère Theo, un marchand d'art connu. Il admire Jean-François Millet, Rembrandt, Frans Hals, Anton Mauve et Eugène Delacroix, tout en s'inspirant d'Hiroshige, Claude Monet, Adolphe Monticelli, Paul Cézanne, Edgar Degas et Paul Signac.<br />
</p>
