<h1>Wassily Kandinsky</h1>
<p>Vassily Kandinsky (en russe : Василий Васильевич Кандинский, Vassili Vassilievitch Kandinski1), né à Moscou le 4 décembre (22 novembre) 1866 et mort à Neuilly-sur-Seine, le 13 décembre 1944, est un peintre, graveur, théoricien de l’art, poète et dramaturge allemand puis français, d'origine russe.<br />
<br />
Considéré comme l’un des artistes les plus importants du xxe siècle aux côtés, notamment, de Picasso et de Matisse, il est un des fondateurs de l'art abstrait : il est généralement considéré comme étant l’auteur de la première œuvre non figurative de l’histoire de l’art moderne, une aquarelle de 1910 qui sera dite « abstraite2 ». Certains historiens ou critiques d'art ont soupçonné Kandinsky d'avoir antidaté cette aquarelle pour s'assurer la paternité de l'abstraction, au motif qu'elle ressemble à une esquisse de sa Composition VII, de 19133.<br />
<br />
Cette aquarelle, qui serait le premier tableau abstrait au monde, a soulevé controverses et discussions, et suscité bien des thèses contradictoires. Deux tendances s'opposent : celle qui la date réellement de 1910, et qui l'inclut dans une série d'études pour Composition VII, celle qui rejette cette datation et la rapproche des œuvres et du style de 1913. Plaident pour cette dernière thèse le format inhabituel et trop grand pour l'époque, et surtout une inscription tardive de cette œuvre dans le registre manuscrit que tient régulièrement Kandinsky à partir de 19194.</p>
